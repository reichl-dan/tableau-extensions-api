/**
 * The different types of time periods that can be used in relative date filters.
 */
export enum PeriodType {
  /**
   * Year period
   */
  Year = 'year',
  /**
   * Quarter period
   */
  Quarter = 'quarter',
  /**
   * Month period
   */
  Month = 'month',
  /**
   * Week period
   */
  Week = 'week',
  /**
   * Day period
   */
  Day = 'day',
  /**
   * Hour period
   */
  Hour = 'hour',
  /**
   * Minute period
   */
  Minute = 'minute',
  /**
   * Second period
   */
  Second = 'second',
}
