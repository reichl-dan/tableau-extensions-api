/**
 * Represents the different types of encoding that can be applied to marks in a visualization.
 */
export enum EncodingType {
  /**
   * Angle encoding
   */
  Angle = 'angle',
  /**
   * Color encoding
   */
  Color = 'color',
  /**
   * Column encoding
   */
  Column = 'column',
  /**
   * Custom encoding
   */
  Custom = 'custom',
  /**
   * Detail encoding
   */
  Detail = 'detail',
  /**
   * Filter encoding
   */
  Filter = 'filter',
  /**
   * Geometry encoding
   */
  Geometry = 'geometry',
  /**
   * Label encoding
   */
  Label = 'label',
  /**
   * Marks type encoding
   */
  MarksType = 'marks-type',
  /**
   * Measure values encoding
   */
  MeasureValues = 'measure-values',
  /**
   * Page encoding
   */
  Page = 'page',
  /**
   * Path encoding
   */
  Path = 'path',
  /**
   * Row encoding
   */
  Row = 'row',
  /**
   * Shape encoding
   */
  Shape = 'shape',
  /**
   * Size encoding
   */
  Size = 'size',
  /**
   * Tooltip encoding
   */
  Tooltip = 'tooltip',
}
