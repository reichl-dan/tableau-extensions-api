/**
 * Represents the type of trend line model that can be applied to data.
 */
export enum TrendLineModelType {
  /**
   * Linear trend line model
   */
  Linear = 'linear',
  /**
   * Logarithmic trend line model
   */
  Logarithmic = 'logarithmic',
  /**
   * Exponential trend line model
   */
  Exponential = 'exponential',
  /**
   * Polynomial trend line model
   */
  Polynomial = 'polynomial',
  /**
   * Power trend line model
   */
  Power = 'power',
}
