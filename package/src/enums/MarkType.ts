/**
 * Represents the different types of marks that can be used in a visualization.
 */
export enum MarkType {
  /**
   * Area mark type
   */
  Area = 'area',
  /**
   * Bar mark type
   */
  Bar = 'bar',
  /**
   * Circle mark type
   */
  Circle = 'circle',
  /**
   * Gantt Bar mark type
   */
  GanttBar = 'gantt-bar',
  /**
   * Line mark type
   */
  Line = 'line',
  /**
   * Map mark type
   */
  Map = 'map',
  /**
   * Pie mark type
   */
  Pie = 'pie',
  /**
   * Polygon mark type
   */
  Polygon = 'polygon',
  /**
   * Shape mark type
   */
  Shape = 'shape',
  /**
   * Square mark type
   */
  Square = 'square',
  /**
   * Text mark type
   */
  Text = 'text',
}
