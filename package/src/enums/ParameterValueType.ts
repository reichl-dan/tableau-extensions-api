/**
 * The different types a parameter value can have.
 */
export enum ParameterValueType {
  /**
   * Boolean parameter type
   */
  Bool = 'bool',
  /**
   * Date parameter type
   */
  Date = 'date',
  /**
   * DateTime parameter type
   */
  DateTime = 'date-time',
  /**
   * Float parameter type
   */
  Float = 'float',
  /**
   * Integer parameter type
   */
  Int = 'int',
  /**
   * String parameter type
   */
  String = 'string',
}
