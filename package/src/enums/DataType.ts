/**
 * The different types of data a value can have.
 */
export enum DataType {
  /**
   * Boolean data type
   */
  Bool = 'bool',
  /**
   * Date data type
   */
  Date = 'date',
  /**
   * DateTime data type
   */
  DateTime = 'date-time',
  /**
   * Float data type
   */
  Float = 'float',
  /**
   * Integer data type
   */
  Int = 'int',
  /**
   * Spatial data type
   */
  Spatial = 'spatial',
  /**
   * String data type
   */
  String = 'string',
  /**
   * Unknown data type
   */
  Unknown = 'unknown',
}
